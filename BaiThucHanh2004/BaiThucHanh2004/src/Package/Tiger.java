package Package;

public class Tiger extends Animal {
  public void Eat(){
    System.out.println("Tiger eats meat");
  }
  public void Sleep(){
    System.out.println("Tiger sleeps 18 hours a day");
  }
  public void Walk(){
    System.out.println("Tiger walks on 4 legs");
  }
  public void Run(){
    System.out.println("Tiger run fast");
  }
  public void Roar(){
    System.out.println("Tiger roars very loudly");
  }
}